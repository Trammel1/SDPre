public class Objective3Lab2 {
  public static void main(String[] args) {
    double side1, side2, hypotenuse;
// Init variables
    side1 = 52;
    side2 = 32;
// Calculate hypotenuse
    hypotenuse = Math.sqrt((side1 * side1) + (side2 * side2));
// Print result
    System.out.println(hypotenuse);
  }
}
